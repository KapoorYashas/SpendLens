// ============================================================
// SpendLens — POST /api/leads
// Saves lead to Supabase, sends email via Resend.
// Honeypot: if `website` field is filled, silently return 200.
// Rate limiting is handled by middleware.ts.
// ============================================================

import { NextRequest, NextResponse } from 'next/server'
import { Resend } from 'resend'
import { getServiceClient } from '@/lib/supabase'
import type { LeadApiRequest, LeadApiResponse, AuditRow } from '@/lib/types'


