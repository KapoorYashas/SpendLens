export async function POST(
  request: NextRequest,
): Promise<NextResponse<LeadApiResponse | { error: string }>> {
  try {
    const body = (await request.json()) as LeadApiRequest

    // Honeypot check — bots fill all fields
    if (body.website && body.website.trim().length > 0) {
      // Silently return success; do nothing
      return NextResponse.json({ success: true })
    }

    // Validate required fields
    if (!body.email || !body.email.includes('@')) {
      return NextResponse.json({ error: 'Valid email is required' }, { status: 400 })
    }
    if (!body.auditId) {
      return NextResponse.json({ error: 'Audit ID is required' }, { status: 400 })
    }

    const supabase = getServiceClient()

    // Fetch audit to get savings data for email
    const { data: audit, error: auditErr } = await supabase
      .from('audits')
      .select('*')
      .eq('id', body.auditId)
      .single()

    if (auditErr || !audit) {
      return NextResponse.json({ error: 'Audit not found' }, { status: 404 })
    }

    // Save lead
    const { error: leadErr } = await supabase.from('leads').insert({
      audit_id: body.auditId,
      email: body.email,
      company: body.company ?? null,
      role: body.role ?? null,
    })

    if (leadErr) {
      console.error('Lead insert error:', leadErr)
      return NextResponse.json({ error: 'Failed to save lead' }, { status: 500 })
    }

    // Send email via Resend (lazy init to avoid build-time key check)
    const auditUrl = `${process.env.NEXT_PUBLIC_SITE_URL ?? 'https://YOUR_VERCEL_URL'}/audit/${body.auditId}`
    const resend = new Resend(process.env.RESEND_API_KEY)
    await resend.emails.send({
      from: 'SpendLens Audit <audit@resend.dev>',
      to: body.email,
      subject: `Your AI spend audit — $${audit.total_monthly_savings}/mo in potential savings`,
      html: buildEmailHtml(audit as AuditRow, auditUrl),
    })

    return NextResponse.json({ success: true })
  } catch (err) {
    console.error('Leads route error:', err)
    return NextResponse.json({ error: 'Internal server error' }, { status: 500 })
  }
}
